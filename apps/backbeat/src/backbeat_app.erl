-module('backbeat_app').

-behaviour(application).

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    lager:start(),
    {ok, Pid} = backbeat_sup:start_link(),
    ignored = bb_wav:load_sounds(),
    {ok, Pid}.

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

